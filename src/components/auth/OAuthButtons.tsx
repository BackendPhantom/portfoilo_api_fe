export function GoogleOAuthButton() {
  const [loading, setLoading] = useState(false);

  const handleClick = async () => {
    setLoading(true);
    try {
      const { data } = await api.get("/auth/social/urls/", {
        withCredentials: true,
      });
      window.location.href = data.google;
    } catch {
      toast.error("Failed to start Google sign-in");
      setLoading(false);
    }
  };

  return (
    <button
      type="button"
      onClick={handleClick}
      disabled={loading}
      className="flex h-11 w-full items-center justify-center gap-3 rounded-lg border border-surface-700 bg-surface-800/50 text-sm font-medium text-surface-200 transition-colors hover:bg-surface-800 hover:border-surface-600 disabled:opacity-50 disabled:cursor-not-allowed cursor-pointer">
      {loading ? <Loader2 className="h-5 w-5 animate-spin" /> : <GoogleIcon />}
      Continue with Google
    </button>
  );
}

